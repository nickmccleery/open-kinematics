    _t4 = z1 - z2
    _t5 = -_t4
    _t6 = 1 / math.sqrt(_EPS_SQ + _t1**2 + _t3**2 + _t5**2)
    return np.array([_t0 * _t6, _t2 * _t6, _t4 * _t6, _t1 * _t6, _t3 * _t6, _t5 * _t6])


